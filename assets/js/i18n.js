  /* ── Estado atual da língua ── */
  let currentLang = localStorage.getItem('bestai_lang') || 'pt';
  
  /* ── Aplica traduções a todos os elementos com data-i18n ── */
  function applyTranslations(lang) {
    currentLang = lang;
    localStorage.setItem('bestai_lang', lang);
  
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang][key] !== undefined) {
        el.innerHTML = translations[lang][key];
      }
    });
  
    // Atualiza botões do switcher
    document.querySelectorAll('.lang-switcher span').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  
    // Atualiza atributo lang do HTML
    document.documentElement.lang = lang;
  }
  
  /* ── Inicializa quando o DOM estiver pronto ── */
  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
  
    document.querySelectorAll('.lang-switcher span').forEach(btn => {
      btn.addEventListener('click', () => {
        applyTranslations(btn.dataset.lang);
      });
    });
  });