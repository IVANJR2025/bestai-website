  /* ── Estado ── */
  let currentLang = localStorage.getItem('bestai_lang') || 'pt';
  
  /* ── Aplica traduções ── */
  function applyTranslations(lang) {
    currentLang = lang;
    localStorage.setItem('bestai_lang', lang);
  
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key] !== undefined) {
        el.innerHTML = translations[lang][key];
      }
    });
  
    /* Atualiza TODOS os botões de língua na página */
    document.querySelectorAll('[data-lang]').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
  
    document.documentElement.lang = lang;
  }
  
  /* ── Init ── */
  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
  
    /* Captura cliques em QUALQUER elemento com data-lang */
    document.addEventListener('click', e => {
      const btn = e.target.closest('[data-lang]');
      if (btn) applyTranslations(btn.getAttribute('data-lang'));
    });
  });